public class gettingStarted_8_Loop {
    public static void main(String[] args) {
        int i=1;
        while(i<=9)
        {
            System.out.println(i);
            i++;
        }
        System.out.println("DONE While Loop!");

        for( i=1; i<=9;i++)
        {
            System.out.println(i);
        }
        System.out.println("DONE for loop!");
    }
}